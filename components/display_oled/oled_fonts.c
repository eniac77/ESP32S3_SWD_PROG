const uint8_t *oled_font_glyph(char c)
{
    uint8_t idx;
    /* Teljes nyomtatható ASCII: 0x20..0x7E. A tartományon kívüli kód -> szóköz. */
    if ((uint8_t)c < 0x20 || (uint8_t)c > 0x7E) idx = 0;   /* nem támogatott -> szóköz */
    else idx = (uint8_t)c - 0x20;
    return OLED_FONT5X7[idx];
}

